    private void anonUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_anonUserActionPerformed
        
        JCheckBox src = (JCheckBox) evt.getSource();
        
        if(src.isSelected()){
            user.setEnabled(false);
            passwd.setEnabled(false);
        }
        else{
            user.setEnabled(true);
            passwd.setEnabled(true);
        }
    }//GEN-LAST:event_anonUserActionPerformed

