    private void acceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptActionPerformed
        DBManager dbmanager;
        
        if(!localhost.isSelected()){
            if(!anonUser.isSelected()){
                dbmanager = new DBManager(url.getText(), user.getText(), passwd.getText());
            }
        
            else{
                dbmanager = new DBManager(url.getText());
            }
        }
        
        else{
            if(!anonUser.isSelected()){
                dbmanager = new DBManager(user.getText(), passwd.getText());
            }

            else{
                dbmanager = new DBManager();
            }
        }
        
        Connection conn = dbmanager.getConnection();
        if(conn!=null){
            parent.setDbConnected(true);
            parent.setDbmanager(dbmanager);
            
            if(dbmanager.checkPrivileges()){
                parent.setAdmin();
                parent.getLabelConnStatus().setText("Estado de Conexión: Admin");   
            }
            
            else{
                parent.setNormalUser();
                parent.getLabelConnStatus().setText("Estado de Conexión: Conectado");    
                
            }           
            JOptionPane.showMessageDialog(rootPane, "Conexión exitosa", "Éxito", JOptionPane.INFORMATION_MESSAGE);   
            this.dispose();
        }
        else{
            parent.setNormalUser();
            parent.setDbConnected(false);
            parent.setDbmanager(null);
            parent.getLabelConnStatus().setText("Estado de Conexión: Desconectado");
            JOptionPane.showMessageDialog(rootPane, "Error al conectar con la base de datos. Comprueba la url y las credenciales", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_acceptActionPerformed

    
